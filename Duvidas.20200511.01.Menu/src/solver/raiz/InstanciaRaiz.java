package solver.raiz;

public class InstanciaRaiz {
	
	private double numero;
	private double indice;
	
	public InstanciaRaiz(double numero, double indice) {
		this.numero = numero;
		this.indice = indice;
	}
	
	public double getNumero() {
		return numero;
	}
	public double getIndice() {
		return indice;
	}
	

}
